

def is_item_available(item_id:int, trans_list:list)->bool:
    filtered = [item for item in trans_list if item['id'] == item_id]
    count = sum([item['quantity'] if item['movement'] == 'in' else -item['quantity'] for item in filtered])
    return count > 0


